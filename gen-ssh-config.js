#!/usr/bin/env node
/**
 * Step 0 — Generate SSH config for every domain
 * ----------------------------------------------
 * You still have to gather Host/Port/User from SiteGround's SSH Keys Manager
 * yourself — but only ONCE PER HOSTING ACCOUNT, not once per domain. List
 * which domains live under each account in ssh-accounts.json (see
 * ssh-accounts.example.json) and this generates a `Host <domain>` block for
 * every single one of them, all reusing the same Host/Port/User/IdentityFile
 * for the account they belong to.
 *
 * Output goes to ~/.ssh/config.d/sitegen-hosts.conf (safe to regenerate —
 * this script only ever overwrites that one generated file) and it makes
 * sure ~/.ssh/config has an `Include` line pointing at it. Anything you've
 * hand-written in ~/.ssh/config is left alone.
 *
 * Usage:
 *   node gen-ssh-config.js [ssh-accounts.json] [--identity=~/.ssh/id_ed25519]
 */

const fs = require('fs');
const os = require('os');
const path = require('path');

function main() {
  const args = process.argv.slice(2);
  const inputPath = args.find((a) => !a.startsWith('--')) || 'ssh-accounts.json';
  const identityArg = args.find((a) => a.startsWith('--identity='));
  const identityFile = identityArg ? identityArg.split('=')[1] : '~/.ssh/id_ed25519';

  if (!fs.existsSync(inputPath)) {
    console.error(`✗ ${inputPath} not found. Copy ssh-accounts.example.json to ${inputPath} and fill in your accounts.`);
    process.exit(1);
  }

  const accounts = JSON.parse(fs.readFileSync(inputPath, 'utf8'));
  if (!Array.isArray(accounts) || !accounts.length) {
    console.error(`✗ ${inputPath} must be a non-empty array of accounts.`);
    process.exit(1);
  }

  const sshDir = path.join(os.homedir(), '.ssh');
  const includeDir = path.join(sshDir, 'config.d');
  const includeFile = path.join(includeDir, 'sitegen-hosts.conf');
  const mainConfig = path.join(sshDir, 'config');

  fs.mkdirSync(includeDir, { recursive: true });

  const blocks = [];
  let domainCount = 0;
  for (const acct of accounts) {
    const { host, port, user, domains } = acct;
    if (!host || !port || !user || !Array.isArray(domains) || !domains.length) {
      console.error(`✗ Skipping malformed account entry: ${JSON.stringify(acct)}`);
      continue;
    }
    for (const domain of domains) {
      domainCount += 1;
      blocks.push(
        `Host ${domain}\n` +
        `    HostName ${host}\n` +
        `    Port ${port}\n` +
        `    User ${user}\n` +
        `    IdentityFile ${identityFile}\n`
      );
    }
  }

  const generated =
    '# Auto-generated by gen-ssh-config.js - do not edit by hand.\n' +
    `# Regenerate from ${path.basename(inputPath)} instead; this file is fully overwritten each run.\n\n` +
    blocks.join('\n');

  fs.writeFileSync(includeFile, generated, 'utf8');
  console.log(`✔ Wrote ${domainCount} Host block(s) → ${includeFile}`);

  // Make sure ~/.ssh/config includes it, without disturbing anything else there.
  const includeLine = `Include ${includeFile.replace(/\\/g, '/')}`;
  let mainContent = fs.existsSync(mainConfig) ? fs.readFileSync(mainConfig, 'utf8') : '';
  if (!mainContent.includes(includeFile.replace(/\\/g, '/')) && !mainContent.includes('config.d/sitegen-hosts.conf')) {
    mainContent = `${includeLine}\n\n${mainContent}`;
    fs.writeFileSync(mainConfig, mainContent, 'utf8');
    console.log(`✔ Added "${includeLine}" to the top of ${mainConfig}`);
  } else {
    console.log(`  (${mainConfig} already includes it)`);
  }

  console.log(`\nTest any of them with:  ssh ${accounts[0].domains[0]}`);
}

main();
